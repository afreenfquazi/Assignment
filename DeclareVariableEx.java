package com.practice.assignment1;

public class DeclareVariableEx {

	public static void main(String[] args) {
		int num1 = 12;
		float num2 = 32.5f;
		double num3 = 23.87900;
		long num4 = 234564324L;
		System.out.println("int value is " +num1);
		System.out.println("float value is " +num2);		
		System.out.println("double value is " +num3);
		System.out.println("long value is " +num4);
	}

}
